<template>
  <div class="customers container">
     <Alert v-if="alert" v-bind:message="alert" />
    <h1 class="page-header">manage cusomter</h1>
    <input class="form-control" placeholder="filtre bu enter last name" v-model="filterInput">
    <br>

    <table class="table table-striped">
      <thead>
        <tr>
          <th>first name</th>
          <th>last name</th>
          <th>email</th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="customer in filterBy(customers, filterInput)">
          <td>{{customer.first_name}}</td>
          <td>{{customer.last_name}}</td>
          <td>{{customer.email}}</td>
          <td><router-link class="btn btn-default" v-bind:to="'/customer/' +customer.id"> view </router-link></td>
        </tr>
      </tbody>
    </table>

  </div>
</template>

<script>
import Alert from './Alert'
export default {
  name: 'customers',
  data () {
    return {
      customers: [],
      alert:'',
      filterInput:''

    }
  },
  methods: {
    fecthCustomers(){
      this.$http.get('http://localhost/traversy/slim/public/index.php/api/customers')
          .then(function(response){
            this.customers = response.body;
          });

    },
    filterBy(list,value) {
      
      return list.filter(function(customer) {
        return customer.last_name.indexOf(value) >-1;
      });
    }
  },
  created: function() {
    if(this.$route.query.alert) {
      this.alert = this.$route.query.alert;
    }
    this.fecthCustomers();
  },
  updated: function() {
    this.fecthCustomers();
  },

  components: {
    Alert
  }
}
</script>

